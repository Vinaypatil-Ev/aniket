# WAP to demonstrate arithmetic operation on two integer numbers.
print("Enter two digits")
a = float(input("Enter first digit: "))
b = float(input("Enter second digit: "))

print(f"{a} + {b} = {a + b}")  # addition
print(f"{a} - {b} = {a - b}")  # substraction
print(f"{a} * {b} = {a * b}")  # mul
print(f"{a} / {b} = {a / b}")  # div
print(f"{a} % {b} = {a % b}")  # mod